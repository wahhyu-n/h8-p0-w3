function graduates(students) {
  // Code disini
  var kelas = {};
  //var kelas = [];
  for (a = 0; a < students.length; a++) {
    if (kelas[students[a].class] === undefined) {
      kelas[students[a].class] = []//hrs array
    }
    if (students[a].score >= 75) {
        kelas[students[a].class].push({
          name: students[a].name,
          score: students[a].score
        })
      
    }
    //obj.push(kelas)
  }

  // console.log(result)
  return kelas
}


